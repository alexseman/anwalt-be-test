<?php

declare(strict_types=1);

namespace App\Domain\Post\Service\Dto;

class CreateUpdateResponse
{
    private int $id;

    /**
     * @param int $id
     */
    public function __construct(int $id)
    {
        $this->id = $id;
    }

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

}
